package com.Chapter_1;

public class Ch1_3_Variables {
    public static void main(String[] args) {
        int num1 = 1;
        int num2 = 2;
        int num3 = 3;
        int sum = num1+num2+num3;
        System.out.print("The sum of num1, num2 and num3 is: ");
        System.out.print(sum);
    }
}

