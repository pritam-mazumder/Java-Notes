package com.Chapter_3_STRING;

public class Ch3_3_Escape_sequence_character {
    public static void main(String[] args) {

        System.out.println("Escape Sequence Character");

        System.out.println("I am \"Gold Nova 1\"");             // \" is used for double quotes
        System.out.println("I \nam \nGold \nNova \n1");         // \n is used for new line
        System.out.println("I am Gold Nova 1 \\ CSGO");         // \\ is used frp single slash
        System.out.println("\tI am Gold Nova 1");               // \t is used for single tab space
        System.out.println("I am \'Gold Nova 1\'");             // \' is used for single quotes
    }
}
